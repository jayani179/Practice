package javaif_else_H.W;

public class Even_Odd {

    public static void main(String[] args) {
        int num=16;
        int result=num%2;

        if(result==0)
        {
            System.out.println("The number is even");
        }
        else
        {
            System.out.println("The number is odd");
        }
    }
}
